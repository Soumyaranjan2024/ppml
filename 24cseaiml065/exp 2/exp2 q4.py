# Create a tuple
my_tuple = ("red", "green", "blue", 9, 26.5)

# Display the entire tuple
print("The complete tuple:", my_tuple)

# Display individual elements using indexing
print("First element:", my_tuple[0])
print("Third element:", my_tuple[2])

# Iterate and display elements using a loop
print("\nElements in the tuple:")
for element in my_tuple:
    print(element)